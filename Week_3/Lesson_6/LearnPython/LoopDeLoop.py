#Loop through a range of numbers from 1 to 4
for x in range(5):
    print(x)

#Loop through a range of numbers from 2 to 6
for x in range(2, 7):
    print(x)

#Loop through the letters in a string
word = "Peace"
for x in word:
    print(x)

#Loop through a list
zoo = ["cow", "dog", "horse", "zebra"]
for x in zoo:
    print(x)

#Loop while a condition is being met
run = "y"

while run == "y":
    print("Hi!")
    run = input("To run again, enter 'y': ")
